let currentLocale: Locale = "en";
const listeners = new Set<(l: Locale) => void>();

function loadLocale(): Locale {
  if (typeof window === "undefined") return "en";
  try {
    const stored = window.localStorage.getItem("gui-locale") as Locale | null;
    if (stored === "en" || stored === "pt") return stored;
  } catch {
    // private mode / storage disabled
  }
  // Auto-detect from navigator
  const nav = navigator.language?.toLowerCase() ?? "";
  return nav.startsWith("pt") ? "pt" : "en";
}

export function getLocale(): Locale {
  return currentLocale;
}

export function setLocale(l: Locale): void {
  currentLocale = l;
  if (typeof window !== "undefined") {
    try {
      window.localStorage.setItem("gui-locale", l);
    } catch {
      // ignore
    }
  }
  listeners.forEach((fn) => fn(l));
}

export function useLocale(): Locale {
  const [locale, setLocaleState] = useState<Locale>("en");
  useEffect(() => {
    const initial = loadLocale();
    currentLocale = initial;
    setLocaleState(initial);
    const fn = (l: Locale): void => setLocaleState(l);
    listeners.add(fn);
    return () => {
      listeners.delete(fn);
    };
  }, []);
  return locale;
}

export function useT(): (key: Key, params?: Record<string, string | number>) => string {
  const locale = useLocale();
  return (key, params) => {
    let s = (DICT[locale][key] ?? DICT.en[key] ?? key) as string;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        s = s.replace(`{${k}}`, String(v));
      }
    }
    return s;
  };
}
