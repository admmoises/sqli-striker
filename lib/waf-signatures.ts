/**
 * WAF fingerprint database.
 *
 * Each entry maps a WAF vendor name to response signatures that
 * help identify it. When sqlmap probes a target, we parse the
 * response headers, body, and status codes against these patterns.
 *
 * Sources: sqlmap's own WAF detection, wafw00f, manual research.
 */

export interface WafSignature {
  /** Human-readable WAF name */
  name: string;
  /** Known product/company names */
  vendor: string;
  /** Response headers that indicate this WAF */
  headers: Record<string, string>;
  /** HTTP status codes commonly returned when blocked */
  codes: number[];
  /** Body regex patterns that indicate blocking by this WAF */
  bodyPatterns: string[];
  /** Cookies set by this WAF */
  cookies: string[];
  /** CSS selectors / HTML markers in block pages */
  htmlMarkers: string[];
}

export interface WafDetectionResult {
  detected: boolean;
  name: string | null;
  vendor: string | null;
  confidence: number; // 0-100
  evidence: string[];
  allMatches: Array<{ name: string; confidence: number }>;
}

