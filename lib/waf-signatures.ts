/**
 * Analyze an HTTP response for WAF signatures.
 * Accepts a simplified response shape so it works with both
 * raw HTTP and sqlmap's output.
 */
export function detectWaf(response: {
  status: number;
  headers: Record<string, string>;
  body: string;
  setCookie?: string[];
}): WafDetectionResult {
  const matches: Array<{ name: string; confidence: number }> = [];
  const evidence: string[] = [];

  const headerKeys = Object.keys(response.headers).map((k) => k.toLowerCase());
  const bodyLower = response.body.toLowerCase();

  for (const sig of WAF_SIGNATURES) {
    let score = 0;
    let maxScore = 0;
    const localEvidence: string[] = [];

    // Check status codes
    maxScore += 15;
    if (sig.codes.includes(response.status)) {
      score += 15;
      localEvidence.push(`HTTP ${response.status} matches known block code`);
    }

    // Check headers
    for (const [hName, hValue] of Object.entries(sig.headers)) {
      maxScore += 10;
      const key = hName.toLowerCase();
      if (headerKeys.includes(key)) {
        const actualValue = response.headers[key] ?? response.headers[hName] ?? "";
        if (hValue === "" || actualValue.toLowerCase().includes(hValue.toLowerCase())) {
          score += 10;
          localEvidence.push(`Header ${hName}: ${actualValue}`);
        }
      }
    }

    // Check body patterns
    for (const pattern of sig.bodyPatterns) {
      maxScore += 12;
      try {
        if (new RegExp(pattern, "i").test(bodyLower)) {
          score += 12;
          localEvidence.push(`Body matches: ${pattern.slice(0, 50)}`);
          break; // One body match is enough for scoring
        }
      } catch {
        // Invalid regex — skip
      }
    }

    // Check cookies
    for (const cookie of sig.cookies) {
      maxScore += 8;
      const cookieLower = cookie.toLowerCase();
      const allCookies = response.setCookie ?? [];
      const cookieHeader = response.headers["set-cookie"] ?? "";
      const allCookieText = [...allCookies, cookieHeader].join(";").toLowerCase();
      if (new RegExp(cookieLower, "i").test(allCookieText)) {
        score += 8;
        localEvidence.push(`Cookie matches: ${cookie}`);
      }
    }

    // Check HTML markers
    for (const marker of sig.htmlMarkers) {
      maxScore += 5;
      if (bodyLower.includes(marker.toLowerCase())) {
        score += 5;
        localEvidence.push(`HTML marker: ${marker}`);
      }
    }

    if (score > 0) {
      const confidence = Math.min(100, Math.round((score / Math.max(1, maxScore)) * 100));
      matches.push({ name: sig.name, confidence });
      if (confidence >= 30) {
        evidence.push(...localEvidence);
      }
    }
  }

  matches.sort((a, b) => b.confidence - a.confidence);

  const top = matches[0];
  if (top && top.confidence >= 30) {
    const sig = WAF_SIGNATURES.find((s) => s.name === top.name);
    return {
      detected: true,
      name: top.name,
      vendor: sig?.vendor ?? null,
      confidence: top.confidence,
      evidence: [...new Set(evidence)],
      allMatches: matches.filter((m) => m.confidence >= 10),
    };
  }

  return {
    detected: false,
    name: null,
    vendor: null,
    confidence: 0,
    evidence: [],
    allMatches: matches.filter((m) => m.confidence >= 10),
  };
}
